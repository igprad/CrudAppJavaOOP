    private void setKategori(){
       int limit = con.getdummyKetegoriBarang().size();
       for(int i=0;i<limit;i++)
        {
           jComboBox3.addItem(con.getdummyKetegoriBarang().get(i).getNama_kategori());
           jComboBox1.addItem(con.getdummyKetegoriBarang().get(i).getNama_kategori());
       }
       for(int i=0;i<con.getdummySupplier().size();i++){
           jComboBox2.addItem(con.getdummySupplier().get(i).getNama_supplier());
           jComboBox4.addItem(con.getdummySupplier().get(i).getNama_supplier());
       }
    }

    
