    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
       if(jTabbedPane1.getSelectedIndex()==0)//DataBarang
        {
            int selectedOption = JOptionPane.showConfirmDialog(null, 
                                      "Apakah Anda Yakin ingin menghapus entri tersebut?", 
                                      "Peringatan", 
                                      JOptionPane.YES_NO_OPTION); 
                if (selectedOption == JOptionPane.YES_OPTION) 
                {
                    con.deleteBarang(setIdDeleteBarang());
                    JOptionPane.showMessageDialog(null, "Data telah dihapus");
                } 
       }
       
        if(jTabbedPane1.getSelectedIndex()==1)//DataSupplier
        {
            
            if(Globaltemp>-1)
            {
                String id_sup=jTable2.getValueAt(Globaltemp,0).toString();
                int selectedOption = JOptionPane.showConfirmDialog(null, 
                                      "Apakah Anda Yakin ingin menghapus entri tersebut?", 
                                      "Peringatan", 
                                      JOptionPane.YES_NO_OPTION); 
                if (selectedOption == JOptionPane.YES_OPTION) 
                {
                    con.deleteSupplier(id_sup);
                    JOptionPane.showMessageDialog(null, "Data telah dihapus");
                    
                } 
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Silahkan pilih data yang hendak di hapus");
            }
            
        }
        showTable();
    }//GEN-LAST:event_jButton5ActionPerformed

