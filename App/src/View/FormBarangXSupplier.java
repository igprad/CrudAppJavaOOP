    private void setEditBarang(){
        int index = jTable1.getSelectedRow();
        pointBarangIndexSelected=jTable1.getSelectedRow();
        jTextField2.setText(jTable1.getValueAt(index, 1).toString());
        jTextField5.setText(jTable1.getValueAt(index, 2).toString());
        jTextField6.setText(jTable1.getValueAt(index, 3).toString());
        jComboBox3.setSelectedItem(jTable1.getValueAt(index, 4));
        jComboBox4.setSelectedItem(jTable1.getValueAt(index, 5));
    }
    
    private int setIdDeleteBarang(){
        return Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(),0).toString());
    }
    
